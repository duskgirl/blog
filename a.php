<p class="text">0.打开命令快捷键：win+r;</p>
<p class="text">1.切换目录 ：D:(磁盘名称+英文冒号，然后回车即可);</p>
<p class="text">2.cd:其功能是显示当前目录的名称/更改当前的目录;</p>
<p class="text"># a.根目录：cd\;</p>
<p class="text"># b.上一层目录：cd..;</p>
<p class="text"># c.下一层目录：cd "目录名称";</p>
<p class="text">3.dir :显示磁盘目录所包括的内容;</p>
<p class="text">4.cls:清屏;</p>
<p class="text">5.上箭头调用历史记录命令;</p>
<p class="text">6.ctrl+c:关闭服务器在cmd上的命令;</p>
<p class="text">7.ipconfig获取本机ip地址</p>
<p class="text">8.mkdir:创建目录</p>


